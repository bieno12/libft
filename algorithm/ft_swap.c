#include "../libft.h"

int	ft_swap(void *it1, void *it2, int size)
{
	int		i;
	char	temp;

	i = -1;
	while (++i < size)
	{
		temp = *(char *)(it1 + i);
		*(char *)(it1 + i) = *(char *)(it2 + i);
		*(char *)(it2 + i) = temp;
	}
	return (1);
}
